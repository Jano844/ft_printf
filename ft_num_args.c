#include "libftprintf.h"

int	ft_num_args(char *format)
{
	int		i;
	int		j;

	i = 0;
	j = 0;
	while (format[i] != '\0')
	{
		if (format[i] == '%')
		{
			i++;
			j++;
		}
		i++;
	}
	return (j);
}
